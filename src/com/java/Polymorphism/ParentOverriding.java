package com.java.Polymorphism;

public class ParentOverriding
{
	public void bunglow()
	{
		System.out.println("Parent bunglow = 02");
	}
	public void car()
	{
		System.out.println("Parent car = 02");
	}
	public void farm()
	{
		System.out.println("Parent farm = 03");
	}
	public void addition(int a,int b)
	{
		int sum =a+b;
		System.out.println("parent sum="+sum);
	}

}
